import binascii

class RemComSvc:
    def __init__(self):
        self.binary = binascii.unhexlify(REMCOMSVC)
        self.offset = 0

    def read(self, amount):
        # Returns amount of bytes and updates the offset within REMCOMSVC variable
        data =  self.binary[self.offset:self.offset+amount]
        self.offset += amount
        return data

    def seek(self, offset):
        self.offset = offset

    def close(self):
        return
        

